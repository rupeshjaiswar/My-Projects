<h1 id="heading4"></span>How to select whom to vote?</h1><br>
<div id="lorem2" >
  <ul>
 <li> Your first step in picking a candidate is to decide the issues you care about and the qualities you want in a leader.  When you consider issues, think about community or national problems that you want people in government to address. Also, consider what party the candidate belongs to and the typical stance the party takes on the issues. For example, you may be interested in the economy; government funding of education or health care; or how they will tackle the unemployment situation.
</li>
 <li> You should also consider leadership qualities. Think about the characteristics you want in an effective leader. Do you look for intelligence, honesty, an ability to communicate, or other factors? Remember that you need to be realistic; nobody is perfect.
</li>
 <li> After considering which issues and qualities are most important to you in the campaign, <b>vote!</b> </li></ul>
